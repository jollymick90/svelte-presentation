<h1>ricapitoliamo i tre momenti nella costruzione di una web app:</h1>

<ul>
  <li>
    Compile Time: The moment when the source code is transformed into another
    form (e.g., JavaScript).
  </li>
  <li>
    Runtime: The moment when the app is loaded and executed in an environment
    (e.g., a browser).
  </li>
  <li>
    Exeution Time: The time during runtime when a specific function or block of
    code is being executed.
  </li>
</ul>
